import Foundation

extension Int {
    internal func format(_ f: String) -> String {
        return String(format: f, self)
    }
}

extension Int8 {
    internal func format(_ f: String) -> String {
        return String(format: f, self)
    }
}

extension UInt8 {
    internal func format(_ f: String) -> String {
        return String(format: f, self)
    }
}

extension UInt32 {
    internal func format(_ f: String) -> String {
        return String(format: f, self)
    }
}
